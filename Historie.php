Van Anton Pieter Maarten Lafeber, geboren 14 maart 1893 in de Vogelenzang (=Wilhelminastraat) te Gouda, zoon van Pieter Lafeber (1855-1928) en Maartje Broekhuizen (1858-1898). In 1970 is Anton Pieter Maarten Lafeber opgenomen in de Ichthus-kliniek te Capelle a/d IJssel waar hij op 17 januari 1972 is overleden. In Gouda werd de "Lafeberhof" aan de Nieuwehaven naar Anton Pieter Maarten Lafeber vernoemd.
