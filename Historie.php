<p>
Anton Pieter Maarten is bekend geworden als de schrijver van een standaardwerk over het Goudse dialect. Dit boek is uitgegeven met medewerking van de oudheidkundige kring "Die Goude" in 1967. In 1965 werd hij werd voor dit werk onderscheiden met de "Zilveren Anjer", uitgereikt door prins Bernard. <br>
